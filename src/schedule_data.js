function chunkArrayInGroups(arr, size) {
	var myArray = [];

	for (let i = 0; i < arr.length; i += size) {
		myArray.push(arr.slice(i, i + size));
	}

	return myArray;
}

const arrayOfRounds = chunkArrayInGroups(arrayOfMatches, 9);

const schedule = {};

